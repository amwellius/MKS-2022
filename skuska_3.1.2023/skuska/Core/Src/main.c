/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	/* USER CODE BEGIN 2 */
	sct_init();
	srand(time(NULL));

	// function for generation random number
	void random_number() {
		int16_t r = rand() % 100;
		if ((r > 10) && (r < 20)) {
			sct_value(919);
		}
		else if ((r > 21) && (r < 30)) {
			sct_value(191);
		}
		else if ((r > 31) && (r < 40)) {
			sct_value(111);
		}
		else if ((r > 41) && (r < 50)) {
			sct_value(292);
		}
		else if ((r > 51) && (r < 60)) {
			sct_value(212);
		}
		else if ((r > 61) && (r < 70)) {
			sct_value(222);
		}
		else {
			sct_value(191);
		}
	}

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		// state automat
		static enum {STATE0, STATE1, STATE2 } state = STATE0;

		// button pressed
		if (HAL_GPIO_ReadPin(S1_GPIO_Port, S1_Pin)==0) {
			state = STATE1;
			HAL_Delay(50);
		}
		if (HAL_GPIO_ReadPin(S2_GPIO_Port, S2_Pin)==0) {
			state = STATE2;
			HAL_Delay(50);
		}

		switch (state) {
		case STATE1: {
			for (int16_t i = 0; i<20; i++) {
				sct_value(999);
				HAL_Delay(20 + (i*2) );

				sct_value(919);
				HAL_Delay(25 + (i*2));

				sct_value(191);
				HAL_Delay(30 + (i*2));

				sct_value(111);
				HAL_Delay(35 + (i*2));

				sct_value(292);
				HAL_Delay(40 + (i*2));

				sct_value(212);
				HAL_Delay(45 + (i*2));

				sct_value(222);
				HAL_Delay(50 + (i*2));
			}

			random_number();
			HAL_Delay(3000);
			// return tu default state
			state = STATE0;
			break;
		}
		case STATE2: {
			random_number();
			HAL_Delay(3000);
			// return tu default state
			state = STATE0;
			break;
		}
		case STATE0: {
			sct_value(999);
			break;
		}
		default: state = STATE0;
		}

		HAL_Delay(50);
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

